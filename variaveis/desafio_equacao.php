<?php

$numeradorA = (6 * (3 + 2))**2;
$denomidadorA = 3 * 2;


$numeradorB = (1 - 5) * (2 - 7);
$denominadorB = 2;

$resultadoA = $numeradorA / $denomidadorA;
$resultadoB = ($numeradorB / $denominadorB) ** 2;

$reusltadoNum = ($resultadoA - $resultadoB) ** 3;
$denominadorFinal = 10 ** 3;

$resultado = $reusltadoNum / $denominadorFinal;

echo $resultado;